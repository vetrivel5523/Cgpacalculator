function showSubjects() {
    var department = document.getElementById("department").value;
    var year = document.getElementById("year").value;
    var semester = document.getElementById("semester").value;


    var subjectsTable = document.getElementById("subjects-body");
    subjectsTable.innerHTML = "";

    var subjects = subjectsData[department][year][semester];

    subjects.forEach(function (subject) {
        var row = "<tr><td>" + subject.subject + "</td><td>" + subject.creditPoints + "</td><td><input type='number' class='grade-point' min='0' max='10'></td></tr>";
        subjectsTable.innerHTML += row;
    });

    document.getElementById("subjects-section").style.display = "block";
}

function calculateCGPA() {
    var department = document.getElementById("department").value;
    var year = document.getElementById("year").value;
    var semester = document.getElementById("semester").value;

    var gradePoints = document.getElementsByClassName("grade-point");
    var totalCreditPoints = 0;
    var totalGradePoints = 0;

    for (var i = 0; i < gradePoints.length; i++) {
        var gradePoint = parseFloat(gradePoints[i].value);
        var creditPoints = parseFloat(gradePoints[i].parentNode.previousElementSibling.textContent);

        if (!isNaN(gradePoint) && gradePoint >= 0 && gradePoint <= 10) {
            totalCreditPoints += creditPoints;
            totalGradePoints += gradePoint * creditPoints;
        } else {
            alert("Please enter valid grade points (0-10) for all subjects.");
            return;
        }
    }

    var cgpa = totalGradePoints / totalCreditPoints;
    document.getElementById("cgpa-result").innerHTML = "<h3>Your CGPA is : " + cgpa.toFixed(2) + "</h3>";
}

function resetInputs() {
    document.getElementById("department").selectedIndex = 0;
    document.getElementById("year").value = "";
    document.getElementById("semester").selectedIndex = 0;


    var subjectsTable = document.getElementById("subjects-body");
    subjectsTable.innerHTML = "";

    document.getElementById("subjects-section").style.display = "none";
    document.getElementById("cgpa-result").innerHTML = "";
}